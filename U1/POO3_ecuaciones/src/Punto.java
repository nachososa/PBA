
public class Punto {

    private float x;
    private float y;

    public Punto(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public void setX(float nuevoValor) {
        this.x = nuevoValor;
    }

    public void setY(float nuevoValor) {
        this.y = nuevoValor;
    }
}
